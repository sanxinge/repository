#! /bin/bash
a="路劲是：`pwd`"
b='路径是：`pwd`'
c=`pwd`
echo ---------------------------------
echo 不加引号且有回车符时，回车符后面的则不能执行,但可以使用反引号\"\` \`\"和转义符\" \\ \"
echo $a
$b
$c
echo ---------------------------------
echo 加双引号则可以有回车换行，且可以通过转义符转换$等符号
echo "$a
\$b
$c"
echo --------------------------------
echo 加单引号也可以有回车换行，但全部输出字符串，\"\`\`\"不起作用也无法转义
echo '$a
$b
$c'
